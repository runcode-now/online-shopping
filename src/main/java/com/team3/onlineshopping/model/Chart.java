package com.team3.onlineshopping.model;

/**
 *
 * @author Admin
 */
public class Chart {

    private int key;
    private double value;

    public Chart() {
    }

    public Chart(int key, double value) {
        this.key = key;
        this.value = value;
    }

    public int getKey() {
        return key;
    }

    public void setKey(int key) {
        this.key = key;
    }

    public double getValue() {
        return value;
    }

    public void setValue(double value) {
        this.value = value;
    }

}
